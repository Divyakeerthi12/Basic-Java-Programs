public class AssignmentOperator {
    public static void main(String args[])
    {
        //Assignment operation, associativity is from right to left
        //+= assignment operator
        int a=10;
        a+=20;
        //*= assignemnt operator
        int b=90;
        b*=10;
        //%= operator
        int c=90;
        c%=8;
        //-=
        int d=60;
        d-=30;
        System.out.println(a+" "+b+" "+c+" "+d+" ");

    }
}
