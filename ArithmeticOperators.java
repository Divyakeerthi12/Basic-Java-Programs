public class ArithmeticOperators {
    public static void main(String args[])
    {
        //Can use any datatype. Here we chose integer datatype
        // Addition
        int a=4+5;
        // Subtraction
        int b=5-4;
        // Multiplication
        int c=5*4;
        // Division
        int d=4/2;
        //Modulo division
        int e=5%2;
        System.out.println(a+" \n"+b+" \n"+c+" \n"+d+" \n"+e+" ");
        //Associativity and precedence
        int result= 3+(1-7)*5%1;
        System.out.println(result);


    }
}
