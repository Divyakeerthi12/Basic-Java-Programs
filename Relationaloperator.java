public class Relationaloperator {
    public static void main(String args[])
    {

        int x=4;
        int y=6;
        int z=4;
        //less than
        boolean a= y<x;
        //less than or equal to
        boolean b=x<=z;
        //greater than
        boolean c= y>z;
        //greater than or equal
        boolean d = z>=x;
        //equal to
        boolean e=x==z;
        //not equal to
        boolean f = y!=z;
        System.out.println(a+" "+b+" "+c+" "+" "+d+" "+e+" "+f);


    }

}
